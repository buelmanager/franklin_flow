import 'package:flutter/material.dart';

class Goal {
  final String emoji;
  final String title;
  final int current;
  final int total;
  final Color color;

  Goal({
    required this.emoji,
    required this.title,
    required this.current,
    required this.total,
    required this.color,
  });

  /// ÏßÑÌñâÎ•† (0.0 ~ 1.0)
  double get progress => total > 0 ? current / total : 0.0;

  /// ÏôÑÎ£å Ïó¨Î∂Ä
  bool get isCompleted => current >= total;

  /// ÎÇ®ÏùÄ ÌöüÏàò
  int get remaining => total - current;

  /// Î≥µÏÇ¨Î≥∏ ÏÉùÏÑ±
  Goal copyWith({
    String? emoji,
    String? title,
    int? current,
    int? total,
    Color? color,
  }) {
    return Goal(
      emoji: emoji ?? this.emoji,
      title: title ?? this.title,
      current: current ?? this.current,
      total: total ?? this.total,
      color: color ?? this.color,
    );
  }
}
